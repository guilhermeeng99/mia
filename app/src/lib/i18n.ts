const translations: Record<UiLocale, Messages> = { en, pt, es, fr, de, it, ja, zh };

const uiLanguagePreference = writable<UiLanguagePreference>("system");

export const uiLocale = derived(uiLanguagePreference, (preference) => resolveUiLocale(preference));
export const i18n = derived(uiLocale, (locale) => translations[locale] ?? translations.en);

uiLocale.subscribe((locale) => {
  if (typeof document !== "undefined") {
    document.documentElement.lang = translations[locale].dateLocale;
  }
});

export function setUiLanguagePreference(preference: UiLanguagePreference) {
  uiLanguagePreference.set(preference);
}

export function resolveUiLocale(preference: UiLanguagePreference, languages?: readonly string[]): UiLocale {
  if (preference !== "system") return preference;
  const candidates =
    languages ?? (typeof navigator === "undefined" ? [] : [navigator.language, ...navigator.languages]);
  for (const language of candidates) {
    const normalized = normalizeLocale(language);
    if (normalized) return normalized;
  }
  return "en";
}

function normalizeLocale(language: string): UiLocale | null {
  const base = language.toLowerCase().split("-")[0];
  if (base === "pt") return "pt";
  if (base === "en") return "en";
  if (base === "es") return "es";
  if (base === "fr") return "fr";
  if (base === "de") return "de";
  if (base === "it") return "it";
  if (base === "ja") return "ja";
  if (base === "zh") return "zh";
  return null;
}

